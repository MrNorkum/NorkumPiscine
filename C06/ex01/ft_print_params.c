#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	x;

	i = 0;
	x = 0;
	while (++x < ac)
	{
		i = -1;
		while (av[x][++i])
			write(1, &av[x][i], 1);
		write(1, "\n", 1);
	}
}
