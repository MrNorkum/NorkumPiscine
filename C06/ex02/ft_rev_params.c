#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	x;

	x = ac;
	while (--x > 0)
	{
		i = -1;
		while (av[x][++i])
			write(1, &av[x][i], 1);
		write(1, "\n", 1);
	}
}
